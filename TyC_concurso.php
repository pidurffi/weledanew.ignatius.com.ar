<h2>Términos  y condiciones</h2>

<p>
1. Para participar tienes que registrarte en el formulario que está en nuestra página.
</p>
<p>
</p>
<p>
2. La duración del concurso es desde el 09 de octubre al 16 de octubre del 2013.
</p>
<p>
3. Podrá concursar cualquier persona residente en Argentina que a través de su registro haya facilitado su nombre completo, dirección de correo electrónico, edad y profesión. Los datos personales requeridos han de ser veraces, de lo contrario se anulará la participación del usuario. La inscripción es gratuita y necesaria para optar al premio.
</p>
<p>
4. El ganador se elegirá mediante la herramienta de selección aleatoria que brinda el programa Random.
</p>
<p>
5. El premio consiste en 5 (cinco) “Kits mamá Weleda” compuestos por: Crema Facial armonizante - Aceite de Masaje para Estrías, Crema de Noche alisante - Contorno de Ojos alisante, Crema de manos Regeneradora de Granada - el Aceite Relax de Lavanda. Crema de Ducha de Citrus - Aceite para Masaje de Árnica. Leche Corporal de Espino Amarillo - Skin Food.
</p>
<p>
6. El nombre de los ganadores se publicará a la finalización del concurso.
</p>
<p>
7. Entrega del premio: El premio se entregará como muy tarde a los 7 días después del cierre de concurso. Las personas legitimadas para poder acceder al premio serán todos aquellos participantes cuyos datos personales sean verdaderos y exactos. Los participantes que no cumplan todos los requisitos que se indican en las bases no tendrán derecho alguno. Se hará entrega del premio enviando por correo o retirando en Weleda SA.
</p>
<p>
8. Protección de datos (Cláusula de privacidad): Le recordamos que los datos facilitados al registrarse como usuario de proyectobamboo.com podrán ser utilizados para promociones publicitarias y prospección comercial.
</p>
<p>
9. Las personas ganadoras serán contactadas mediante correo electrónico a la dirección facilitada en el registro por el usuario. Pasados 21 días no podrá reclamarse el premio.
</p>


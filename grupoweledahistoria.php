            <p>El Grupo Weleda A.G fue fundado en Suiza en 1921 como resultado del trabajo conjunto de un grupo de médicos y farmacéuticos liderados por la Dra. Ita Wegman y el Dr. Rudolf Steiner, que buscaban complementar el tratamiento de la persona como un todo.</p>
            <p>Con el objetivo de estimular las fuerzas naturales contenidas en el ser humano, utilizaron técnicas farmacéuticas combinadas con un acercamiento espiritual, y desarrollaron una línea verdaderamente innovadora de productos para el cuidado de la salud en armonía con el ser humano y la naturaleza.</p>
            <p>Hoy los laboratorios Weleda están presentes en todos los continentes, siguiendo principios idénticos que determinan la calidad de sus cosméticos y medicamentos. Desde la obtención de la materia prima hasta el producto final, todo es meticulosamente controlado, registrado y analizado para preservar la fuerza original de las sustancias naturales y apoyar las fuerzas sanadoras presentes en todo organismo.</p>
            <p>La base de la excelencia de Weleda está tanto en el empleo de las materias primas de origen natural y plantas medicinales cultivadas en campos propios con técnicas orgánicas y biodinámicas; así como en la filosofía de producción de la empresa, que promueve la salud humana, preserva el medio ambiente, no realiza experimentos en animales, ni utiliza aditivos químicos sintéticos, tales como conservantes, estabilizantes y perfumes artificiales.</p>
            <p>En Argentina, Weleda está presente con sus productos desde hace más de 25 años. Cuenta con campos propios en las localidades de Derqui (Buenos Aires) y Villa Berna (Córdoba), cultivados bajo normas de la agricultura biodinámica, que proveen una parte significativa de las plantas utilizadas como materias primas. Posee a su vez, una planta elaboradora, donde desarrolla su extensa línea de productos y una farmacia Belladona en la Ciudad de Buenos Aires, en el barrio de Saavedra, para la elaboración de preparados magistrales antroposóficos y homeopáticos.</p>

